// Crea una función que reciba dos números y devuelva el mayor.

function mayor(numero1, numero2) {
  if (numero1 > numero2) {
    return numero1;
  } else {
    return numero2;
  }
}

// Ejemplo de uso:
console.log(mayor(5, 10)); // Muestra 10
console.log(mayor(20, 7)); // Muestra 20

